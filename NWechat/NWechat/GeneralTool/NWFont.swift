import Foundation
import UIKit
class NWFont{
    class var navBarTitle:UIFont{
        get{ return UIFont.systemFont(ofSize: 17)  }
    }
    
    class var sectionTitle:UIFont {
        get{ return UIFont.systemFont(ofSize: 15) }
    }
    
    class var chatNickname:UIFont {
        get {return UIFont.systemFont(ofSize: 13)}
    }
    
    class var chatTimeLabel:UIFont{
        get {return UIFont.systemFont(ofSize: 11)}
    }
    
    class var chatContent:UIFont {
        get {return UIFont.systemFont(ofSize: 14)}
    }
    
    
    
    
}
